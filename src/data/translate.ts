export function replaceKeysAndValues(obj, jpList, zhList) {
  const mapping = Object.fromEntries(jpList.map((jp, i) => [jp, zhList[i]]));

  function replaceValue(val) {
    if (typeof val === 'string' && mapping.hasOwnProperty(val)) {
      return mapping[val];
    }
    if (Array.isArray(val)) {
      return val.map(item => replaceValue(item));
    }
    if (val && typeof val === 'object') {
      const newObj = {};
      for (const [k, v] of Object.entries(val)) {
        const newKey = mapping.hasOwnProperty(k) ? mapping[k] : k;
        newObj[newKey] = replaceValue(v);
      }
      return newObj;
    }
    return val;
  }

  return replaceValue(obj);
}